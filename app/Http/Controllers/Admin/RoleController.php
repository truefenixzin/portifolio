    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

//        dd($request->all());
        $roles = new Role();
        $roles->name = $request->roles;
        $roles->guard_name ='web';
        $result = $roles->save();
        if (!empty($request->permissions)) {
            foreach ($request->permissions as $permission) {
                $rolePermissions[] = Permission::whereId($permission)->first();
            }
        }

        if (isset($rolePermissions) && !empty($rolePermissions)) {
            $roles->syncPermissions($rolePermissions);
        } else {
            $roles->syncPermissions(null);
        }

        if ($result)
        {
            return redirect()->route('admin.roles.create');
        }

        Echo "deu ruim, chama o TI";
    }

